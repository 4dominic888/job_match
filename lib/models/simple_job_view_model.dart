class SimpleJobView {
  final String timeAgo;
  final String title;
  final String company;
  final String category;
  final String type;
  final String salary;
  final String location;

  SimpleJobView({
    required this.timeAgo,
    required this.title,
    required this.company,
    required this.category,
    required this.type,
    required this.salary,
    required this.location
  });
}